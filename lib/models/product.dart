class Product {
  String farmerName;
  String phone;
  String address;
  String category;
  String productName;
  double price;
  int quantity;

  Product({
    required this.farmerName,
    required this.phone,
    required this.address,
    required this.category,
    required this.productName,
    required this.price,
    required this.quantity,
  });
}